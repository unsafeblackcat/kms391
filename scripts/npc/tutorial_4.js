function action(mode, type, selection) {
    dialogue = [
        "\r\n#r坎島#k為玩家準備了豐富多樣的遊戲內容，現在讓我們了解#e娛樂系統#n吧！",
        "娛樂系統位於#e『快速移動』#n介面的第5個選項，包含抽獎、任務、小遊戲等多種玩法"
	     ];
    if (mode == 1) {
        status++;
    } else {
        cm.dispose();
        return;
    }
    if (cm.getPlayer().getKeyValue(20190721, "tutostatus") == -1) {
        if (status < dialogue.length) {
            cm.sendNext(dialogue[status]);
        } else {
            cm.sendOk(" 覺得練等打怪太單調？推薦體驗#b怪物公園#k！\n請完成1次怪物公園挑戰後回報任務。\r\n\r\n#d ※ 輸入指令 #e@教學#n 可隨時召喚我");
            cm.getPlayer().setKeyValue(20190721, "tutostatus", 0);
            cm.dispose();
        }
    } else {
        getq = cm.getPlayer().getKeyValue(20190721, "tutorial") + 1;
        if (isQuestCompleteable(getq)) {
            cm.sendOk("#b#e 任務完成！#k#n\r\n獎勵發放成功！\r\n\r\n#fUI/UIWindow2.img/QuestIcon/4/0#\r\n#r#e30  坎島點數#n#k");
            cm.getPlayer().setKeyValue(20190721, "tutorial", cm.getPlayer().getKeyValue(20190721, "tutorial") + 1);
            cm.getPlayer().setKeyValue(20190721, "tutostatus", -1);
            itembyQuest(getq);
            cm.dispose();
            return;
        } else {
            cm.sendOk(" 您尚未完成挑戰哦！\n請先完成1次#b怪物公園#k後再回來找我。\r\n\r\n#d ※ 輸入指令 #e@教學#n 可隨時召喚我");
            cm.dispose();
        }
    }
}

